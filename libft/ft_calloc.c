#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size);

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*res;

	if (nmemb == 0 || size == 0)
		return ((void *)0);
	res = malloc(nmemb * size);
	((char *)res)[0] = '\0';
	return (res);
}

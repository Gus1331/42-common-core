#include "libft.h"

t_list	*ft_lstlast(t_list *lst);

t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return ((void *) 0);
	while (lst->next)
		lst = lst->next;
	return (lst);
}
